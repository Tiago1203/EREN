'use client';

import { useRouter } from 'next/navigation';
import type { StatCard as StatCardType } from '../types/dashboard.types';

interface StatCardProps {
  card: StatCardType;
}

/**
 * StatCard - Tarjeta de estadística individual
 */
export function StatCard({ card }: StatCardProps) {
  const router = useRouter();

  const handleClick = () => {
    if (card.href) {
      router.push(card.href);
    }
  };

  return (
    <button
      onClick={handleClick}
      className="card p-6 text-left hover:shadow-md transition-shadow group"
      disabled={!card.href}
    >
      <div className="flex items-start justify-between">
        <div>
          <p className="text-sm font-medium text-[var(--muted)]">{card.title}</p>
          <p className="text-3xl font-bold mt-1">{card.value}</p>
          <p className="text-xs text-[var(--muted)] mt-1">{card.subtitle}</p>
        </div>
        <div className={`${card.color} rounded-lg p-2.5 group-hover:scale-105 transition-transform`}>
          <StatIcon name={card.icon} />
        </div>
      </div>
    </button>
  );
}

