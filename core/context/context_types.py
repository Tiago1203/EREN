"""Type definitions for the Cognitive Context System.

Provides comprehensive type definitions for context, blackboard entries,
and related structures.

Architecture only — no business logic, no AI.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from datetime import datetime, timezone
from enum import Enum, IntEnum
from typing import TYPE_CHECKING, Any

if TYPE_CHECKING:
    pass


# =============================================================================
# Context Status
# =============================================================================


class ContextStatus(IntEnum):
    """Status of the cognitive context."""

    INITIALIZING = 0  # Context being created
    PROCESSING = 1  # Active processing
    PENDING_EVIDENCE = 2  # Waiting for more evidence
    PENDING_HYPOTHESIS = 3  # Waiting for hypotheses
    PENDING_DIAGNOSIS = 4  # Waiting for diagnosis
    COMPLETED = 5  # Processing complete
    FAILED = 6  # Processing failed
    CANCELLED = 7  # Processing cancelled
    EXPIRED = 8  # Context expired


class ProcessingStage(IntEnum):
    """Stage of cognitive processing."""

    INTENTION = 10  # Understanding user intention
    CONTEXT_BUILDING = 20  # Building context
    KNOWLEDGE_RETRIEVAL = 30  # Retrieving knowledge
    MEMORY_RETRIEVAL = 40  # Retrieving memories
    HYPOTHESIS_GENERATION = 50  # Generating hypotheses
    EVIDENCE_COLLECTION = 60  # Collecting evidence
    DIAGNOSIS = 70  # Making diagnosis
    WORKFLOW_SELECTION = 80  # Selecting workflow
    EXECUTION = 90  # Executing plan
    RESPONSE = 100  # Generating response


# =============================================================================
# Confidence Level
# =============================================================================


class ConfidenceLevel(IntEnum):
    """Confidence level for conclusions."""

    NONE = 0  # No confidence
    VERY_LOW = 10  # Speculative
    LOW = 25  # Low confidence
    MODERATE = 50  # Moderate confidence
    HIGH = 75  # High confidence
    VERY_HIGH = 90  # Very high confidence
    CERTAIN = 100  # Certain


@dataclass
class Confidence:
    """Confidence score with metadata."""

    level: ConfidenceLevel = ConfidenceLevel.NONE
    score: float = 0.0  # 0.0 to 1.0
    reasons: tuple[str, ...] = field(default_factory=tuple)
    supporting_evidence: tuple[str, ...] = field(default_factory=tuple)
    contradicting_evidence: tuple[str, ...] = field(default_factory=tuple)

    def is_confident(self, threshold: float = 0.5) -> bool:
        """Check if confidence exceeds threshold."""
        return self.score >= threshold

    def is_certain(self) -> bool:
        """Check if certainty level is high."""
        return self.level >= ConfidenceLevel.HIGH


# =============================================================================
# Evidence and Hypothesis
# =============================================================================


class EvidenceType(str, Enum):
    """Type of evidence."""

    OBSERVATION = "observation"  # Direct observation
    MEASUREMENT = "measurement"  # Quantitative measurement
    HISTORICAL = "historical"  # Historical data
    DOCUMENTED = "documented"  # From documentation
    REPORTED = "reported"  # User reported
    DERIVED = "derived"  # Derived from other evidence
    TEMPORARY = "temporary"  # Temporary hypothesis


class EvidenceSource(str, Enum):
    """Source of evidence."""

    USER = "user"  # User provided
    DEVICE = "device"  # Device data
    SENSOR = "sensor"  # Sensor reading
    KNOWLEDGE_BASE = "knowledge_base"  # From KB
    MEMORY = "memory"  # From memory
    ENGINE = "engine"  # Generated by engine
    MANUAL = "manual"  # From technical manual
    STANDARD = "standard"  # From standards


@dataclass
class Evidence:
    """A piece of evidence in the context."""

    evidence_id: str
    evidence_type: EvidenceType
    source: EvidenceSource
    content: str | dict
    confidence: Confidence = field(default_factory=Confidence)
    timestamp: str = ""
    engine_id: str = ""  # Engine that generated this evidence
    tags: tuple[str, ...] = field(default_factory=tuple)
    metadata: dict = field(default_factory=dict)

    def __post_init__(self) -> None:
        """Set timestamp if not provided."""
        if not self.timestamp:
            self.timestamp = datetime.now(timezone.utc).isoformat()


@dataclass
class Hypothesis:
    """A hypothesis in the context."""

    hypothesis_id: str
    description: str
    probability: float = 0.5  # 0.0 to 1.0
    confidence: Confidence = field(default_factory=Confidence)
    supporting_evidence: tuple[str, ...] = field(default_factory=tuple)  # Evidence IDs
    contradicting_evidence: tuple[str, ...] = field(default_factory=tuple)
    status: str = "active"  # active, confirmed, rejected, pending
    rank: int = 0  # Priority rank (lower is higher priority)
    created_at: str = ""
    engine_id: str = ""
    metadata: dict = field(default_factory=dict)

    def __post_init__(self) -> None:
        """Set timestamp if not provided."""
        if not self.created_at:
            self.created_at = datetime.now(timezone.utc).isoformat()


