# =============================================================================
# Blackboard Entry
# =============================================================================


class BlackboardEntryType(str, Enum):
    """Types of blackboard entries."""

    EVIDENCE = "evidence"
    HYPOTHESIS = "hypothesis"
    OBSERVATION = "observation"
    PLAN = "plan"
    DIAGNOSIS = "diagnosis"
    WORKFLOW = "workflow"
    TOOL = "tool"
    RESPONSE = "response"
    METADATA = "metadata"


@dataclass
class BlackboardEntry:
    """An entry on the cognitive blackboard.

    All motors write to this shared space. Entries are immutable once created
    and can only be superseded by new entries.
    """

    entry_id: str
    entry_type: BlackboardEntryType
    content: Any
    author_engine: str  # Engine that created this entry
    timestamp: str = ""
    version: int = 1
    parent_entry_id: str = ""  # For versioning
    superseded_by: str = ""  # If this entry was superseded
    confidence: Confidence = field(default_factory=Confidence)
    metadata: dict = field(default_factory=dict)

    def __post_init__(self) -> None:
        """Set timestamp if not provided."""
        if not self.timestamp:
            self.timestamp = datetime.now(timezone.utc).isoformat()

    def is_active(self) -> bool:
        """Check if entry is active (not superseded)."""
        return not self.superseded_by


